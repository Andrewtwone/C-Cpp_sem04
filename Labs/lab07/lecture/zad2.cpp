#include <iostream>

using namespace std;

void valueCount(const int tab[], int n, int szukana, int *ile, int *pierwszy, float *srednia)
{
    *ile = 0;
    *pierwszy = -1;
    int suma_indeksow = 0;

    for (int i = 0; i < n; ++i)
    {
        if (tab[i] == szukana)
        {
            (*ile)++;
            suma_indeksow += i;
            if (*pierwszy == -1)
                *pierwszy = i;
        }
    }

    if (*ile > 0)
        *srednia = (float)suma_indeksow / *ile;
    else
        *srednia = -1;
}

